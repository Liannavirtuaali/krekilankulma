<p>Krekilänkulmassa hevosten jalostus on hyvinkin pienimuotoista. Nainen pyrkii yhdistämään sellaisia hevosia jotka täydentävät toistensa puutteita ja sitä kautta tuoda maailmaan hyvän kapasiteetin ja luonteen omaavia yksilöitä. </p>

<p>Ramona ei ota vastaan tilausvarsa kyselyitä. Varsoja syntyy aina kun on jollain tammalla sopiva väli varsomiseen ja sille löydetään sopiva ori. Osa varsoista jää kotiin jatkamaan sukulinjaansa, mutta satunnaisesti joku saattaa tulla myyntiin. Jokainen kasvatti on suunniteltu niin, että se voidaan jättää myös kotiin mikäli sopivaa uutta kotia ei löydy. </p>

