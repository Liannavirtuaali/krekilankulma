<h2>Ratsastusalueet</h2>

<!-- <img src="img/koriste-etusivu.png" style="margin: 30px 0 0 30px; height: 300px;" align="right" class="reunat"> -->

<p>Krekilänkulma ei ehkä <small><i>(vielä)</i></small> omista maneesia, mutta sen puutetta on korvattu panostamalla kenttään. Sen pohja on tehty huolellisesti jotta se pystytään pitämään käyttökelpoisena säästä ja vuodenajasta riippumatta. Se on juuri sen kokoinen, että sinne saa rakennettua tarvittaessa täysipitkän kouluradan. Valaistukseen on myös panostettu ja kentällä näkee hyvin ratsastaa vaikka keskellä talviyötä. </p>

<p>Kentän lisäksi Krekilänkulmassa on erinomaiset maastoreitit. Lähiympäristöstä löytyy isompia ja pienempiä polkuja vaihtelevalla maaston korkeudella, sekä lähellä virtaa joki. Suurinpiirtein varttitunnin kävelymatkan päässä on myös järvi kauniilla sijainnilla. Siellä voi käydä uittamassa hevosia. </p>

<p>Krekilänkulman omistukseen on hankittu myös lähellä sijaitsevan vanhan perunatilan käytöstä poistettu perunavarasto. Rakennuksessa ei ole mitään vikaa - se vain oli sijainniltaan perunatilalle huonossa paikassa. Siitä olisi tarkoitus remontoida tilalle maneesi, mutta muutostöissä tulee menemään aikaa. </p>

