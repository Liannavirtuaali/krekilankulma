<h6>Tallirakennus</h6>

<a href="img/talli.jpg"><img src="img/talli.jpg" style="margin: 25px 30px 0 0; height: 220px;" align="left" class="reunat"></a>

<p>Vanhasta navetasta remontoitu tallirakennus pitää sisällään kymmenen karsinaa, pesukarsinan, rehuvaraston, kuivaushuoneen, yhdistetyn varuste- ja oleskeluhuoneen, sekä kylpyhuoneen. Karsinoiden etuseinät on avoimet ja hevoset voivat kurkistella niiden ylitse tallin tapahtumia. Automaattiset vesikupit helpottavat arkea huomattavasti ja rehusankoille on omat renkaat karsinan etuosassa. Ruoka-aikojen ulkopuolella sankot säilytetään rehuvarastossa ja renkaita voi käyttää silloin hevosen kiinni sitomiseen. Pesukarsinasta löytyy vesiletku, sekä koreja sienien ja pesuaineiden säilytykseen. </p>

<p>Rehuvarastossa väkirehut säilytetään muovisissa kannellisissa astioissa jotta vältytään kutsumattomilta vierailta. Jokaiselle hevoselle on omat astiansa, sillä rehujen hankkimisesta jokainen vastaa itse. Omistajat huolehtivat vuorokauden rehut valmiiksi kannellisiin rasioihin ja nostavat niille tarkoitetuille hyllyille jotta ruokinta sujuu aina mahdollisimman vaivatta. Avattua heinäpaalia säilytetään erillisessä varastossa josta ilta heinät täytetään verkkoihin. Rehuvarastosta pääsee kuivaushuoneeseen jossa on useita loimitelineitä, sekä pienempiä telineitä muiden varusteiden kuivaamiseen. Pesukonetta tallilla ei ole. </p>

<a href="img/tallikaytava.jpg"><img src="img/tallikaytava.jpg" style="margin: 10px 0 0 30px; height: 200px;" align="right" class="reunat"></a>

<p>Varuste-/oleskelutilaan saapuessa tallin kautta vasemmalle puolelle tulee naulakko ja kulmahylly josta löytyy kaikenlaista tavaraa. Oikealle puolelle tulee avohyllykkö jossa voi säilyttää erilaisia hevosten tavaroita. Sen vieressä satulatelineet parissa kerroksessa jotka vanha isäntä on itse rakennellut ja niiden alapuolella säilytystilaa hevosten varusteille. Satulatelineitä vastapäätä on pieni keittonurkkaus joka pitää sisällään vesipisteen, pienen jääkaapin pakastelokerolla, mikron, sekä veden- ja kahvinkeittimet. Yläkaapeista löytää kuppeja, laseja ja pieni määrä muita astioita. Alakaapit on aina pienoinen sekasorto, sillä niihin eksyy aina milloin mitäkin tavaroita. Pirtin pöytä on keittiön ja satula telineiden välissä. </p>

<p>Keittonurkkauksen vieressä on ovi suoraan ulos. Sen toisella puolella on teline johon voi laittaa kenkiä ja takkeja. Suitsien paikat on kylpyhuoneen oven vieressä ja niiden alapuolelta löytyy vielä yksi laatikosto hevosten tavaroille. Kylpyhuoneessa on pöntön, lavuaarin ja suihkukopin lisäksi pieni laatikosto josta löytää vain ihmisten tarvitsemia asioita. Sieltä löytää muun muassa ensiapu tarpeita. </p>

<p>Tallin heinävintille pääsee ramppia ajamalla suoraan myös traktorilla. Vintillä säilötään avaamattomia heinäpaaleja ja rehusäkkejä jotka ei mahdu rehuvaraston laatikoihin. Sieltä löytää myös muutaman kaapin johon voi säilöä vuodenaikaan nähden tarpeettomia loimia ja muita hevosten varusteita. </p>

