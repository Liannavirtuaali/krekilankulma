<h2>Pihapiiri ja rakennukset</h2>

<a href="img/tontti.jpg"><img src="img/tontti.jpg" style="margin: 25px 0 0 30px; height: 190px;" align="right" class="reunat"></a>

<p>Krekilänkulman tontti on kooltansa yllättävänkin iso, mutta rakennettu hevosten hyvinvointia ajatellen. Se pitää sisällään ison omakotitalon, ulkosaunan, aittarakennuksen, kymmenen paikkaisen tallin, ison kentän, sekä useita tilavia tarhoja. Mikään rakennuksista ei ole tällä vuosituhannella rakennettuja, mutta niistä on pidetty vuosien saatossa hyvää huolta ja remontoitu kun sille on tarvetta. Lantalan virkaa toimittaa pihassa kököttävä isohko traktorin peräkärry jonka naapuri käy ystävällisesti tyhjentämässä pellollensa aina kun on tarvetta. </p>

<a href="img/aitta.jpg"><img src="img/aitta.jpg" style="margin: 10px 30px 0 0; height: 190px;" align="left" class="reunat"></a>

<p>Aittarakennus on vasta remontoitu ja se pitää sisällään kaksi pientä identtistä yksiötä vieraille. Pienessä tupakeittiössä on jääkaapin lisäksi kahvin- ja vedenkeittimet ja perus astiat. Keittiön lisäksi löytyy 120cm leveä sänky, pieni vaatekaappi, kahden istuttava sohva, pienehkö televisio sekä wc. Suihkua asunnoissa ei ole, mutta tallin peseytymistilat on vieraiden vapaasti käytettävissä. Aitan yläkerrassa on ainoastaan varastotilaa eikä siellä mahdu kunnolla seisomaan matalalla olevan katon vuoksi. Kovin pitkien ihmisten ei edes suositella menevän sinne ellei halua jäädä jumiin. </p>

<a href="img/kotitalo.jpg"><img src="img/kotitalo.jpg" style="margin: 10px 0 0 30px; height: 170px;" align="right" class="reunat"></a>

<p>Pihalla on vanhemman puoleinen, mutta kaunis hirsirunkoinen omakotitalo johon Ramona asettui asumaan. Talo on vasta remontoitu ja sitä on tuotu enemmän tälle vuosituhannelle. Isoimmat remontit on tehty keittiöön ja kylpyhuoneeseen, sekä pieneen vessaan. Usean makuuhuoneen asunnossa on mukavasti tilaa ja sen pihalla on kotoisasti lapsille keinu ja pieni puutarhapöytä setti jonka ääressä on mukava juoda aamukahvia ja katsella lähitarhan hevosia. Takapihalle on aseteltu terassiryhmä, kaasugrilli ja muita pieniä mukavuuksia vapaailtoihin. </p>

<p>Tallialueella on myös neljän hevosen pihatto josta on muotoutunut orien leikkikenttä. </p>

